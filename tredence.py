# 6. Evaluation
# =========================================================
@torch.no_grad()
def evaluate(model, loader):
    model.eval()
    correct = 0
    total = 0

    for images, labels in loader:
        images = images.to(device, non_blocking=True)
        labels = labels.to(device, non_blocking=True)

        outputs = model(images)
        preds = outputs.argmax(dim=1)

        correct += (preds == labels).sum().item()
        total += labels.size(0)

    return 100.0 * correct / total


# =========================================================
# 7. Training Function
# =========================================================
def train_one_model(lambda_target, epochs=35, warmup_epochs=10):
    model = PrunableMLP().to(device)

    optimizer = torch.optim.AdamW(model.parameters(), lr=1e-3, weight_decay=1e-4)
    scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=epochs)

    best_acc = 0.0
    best_acc_state = None

    best_tradeoff_score = -1e9
    best_tradeoff_state = None

    history = []

    for epoch in range(epochs):
        model.train()

        running_total_loss = 0.0
        running_cls_loss = 0.0
        running_sparse_loss = 0.0

        lambda_eff = lambda_target * min(1.0, (epoch + 1) / warmup_epochs)

        for images, labels in train_loader:
            images = images.to(device, non_blocking=True)
            labels = labels.to(device, non_blocking=True)

            optimizer.zero_grad()

            outputs = model(images)
            cls_loss = F.cross_entropy(outputs, labels)
            sparse_loss = compute_sparsity_loss(model)
            total_loss = cls_loss + lambda_eff * sparse_loss

            total_loss.backward()
            optimizer.step()

            running_total_loss += total_loss.item()
            running_cls_loss += cls_loss.item()
            running_sparse_loss += sparse_loss.item()

        scheduler.step()

        test_acc = evaluate(model, test_loader)
        sparsity = compute_sparsity_percent(model, threshold=1e-2)

        if test_acc > best_acc:
            best_acc = test_acc
            best_acc_state = copy.deepcopy(model.state_dict())

        tradeoff_score = test_acc + 0.20 * sparsity
        if tradeoff_score > best_tradeoff_score:
            best_tradeoff_score = tradeoff_score
            best_tradeoff_state = copy.deepcopy(model.state_dict())

        history.append({
            "epoch": epoch + 1,
            "lambda_target": lambda_target,
            "lambda_effective": lambda_eff,
            "train_total_loss": running_total_loss / len(train_loader),
            "train_cls_loss": running_cls_loss / len(train_loader),
            "train_sparse_loss": running_sparse_loss / len(train_loader),
            "test_accuracy": test_acc,
            "sparsity_percent_1e2": sparsity
        })

        print(
            f"Lambda={lambda_target:.1e} | "
            f"Epoch {epoch+1:02d}/{epochs} | "
            f"Acc={test_acc:.2f}% | "
            f"Sparsity={sparsity:.2f}% | "
            f"Lambda_eff={lambda_eff:.2e}"
        )

    model.load_state_dict(best_tradeoff_state)

    final_acc = evaluate(model, test_loader)
    final_sparsity = compute_sparsity_percent(model, threshold=1e-2)
    final_stats = gate_stats(model)

    return model, pd.DataFrame(history), final_acc, final_sparsity, final_stats


