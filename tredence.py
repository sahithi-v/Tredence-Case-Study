# =========================================================
# 3. Prunable Linear Layer
# =========================================================
class PrunableLinear(nn.Module):
    def __init__(self, in_features, out_features, gate_init=-2.0):
        super().__init__()
        self.in_features = in_features
        self.out_features = out_features

        self.weight = nn.Parameter(torch.empty(out_features, in_features))
        self.bias = nn.Parameter(torch.zeros(out_features))

        # gate_scores has the exact same shape as weight
        self.gate_scores = nn.Parameter(torch.full((out_features, in_features), gate_init))

        nn.init.kaiming_uniform_(self.weight, a=np.sqrt(5))

        fan_in, _ = nn.init._calculate_fan_in_and_fan_out(self.weight)
        bound = 1 / np.sqrt(fan_in)
        nn.init.uniform_(self.bias, -bound, bound)

    def forward(self, x):
        gates = torch.sigmoid(self.gate_scores)
        pruned_weight = self.weight * gates
        return F.linear(x, pruned_weight, self.bias)

    def gate_values(self):
        return torch.sigmoid(self.gate_scores)


# =========================================================
# 4. Prunable MLP Model
# =========================================================
class PrunableMLP(nn.Module):
    def __init__(self):
        super().__init__()
        self.flatten = nn.Flatten()

        self.fc1 = PrunableLinear(32 * 32 * 3, 1024, gate_init=-2.0)
        self.bn1 = nn.BatchNorm1d(1024)

        self.fc2 = PrunableLinear(1024, 512, gate_init=-2.0)
        self.bn2 = nn.BatchNorm1d(512)

        self.fc3 = PrunableLinear(512, 10, gate_init=-2.0)

        self.dropout = nn.Dropout(0.2)

    def forward(self, x):
        x = self.flatten(x)

        x = self.fc1(x)
        x = self.bn1(x)
        x = F.relu(x)
        x = self.dropout(x)

        x = self.fc2(x)
        x = self.bn2(x)
        x = F.relu(x)
        x = self.dropout(x)

        x = self.fc3(x)
        return x


# =========================================================
# 5. Sparsity Utilities
# =========================================================
def compute_sparsity_loss(model):
    total = 0.0
    for module in model.modules():
        if isinstance(module, PrunableLinear):
            total = total + module.gate_values().sum()
    return total

def collect_all_gates(model):
    gates = []
    for module in model.modules():
        if isinstance(module, PrunableLinear):
            gates.append(module.gate_values().detach().cpu().view(-1))
    return torch.cat(gates)

def compute_sparsity_percent(model, threshold=1e-2):
    gates = collect_all_gates(model)
    sparse_count = (gates < threshold).sum().item()
    total_count = gates.numel()
    return 100.0 * sparse_count / total_count

def gate_stats(model):
    gates = collect_all_gates(model).numpy()
    return {
        "min": float(np.min(gates)),
        "max": float(np.max(gates)),
        "mean": float(np.mean(gates)),
        "median": float(np.median(gates)),
        "below_1e2_pct": float((gates < 1e-2).mean() * 100),
        "below_5e2_pct": float((gates < 5e-2).mean() * 100),
        "below_1e1_pct": float((gates < 1e-1).mean() * 100),
    }


